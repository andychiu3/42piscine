#include <unistd.h>
#include <stdio.h>

int	main(int ac, char *av[])
{
	int	i = 0; // location

	if (ac == 2)
	{
		while ((av[1][i]) != '\0')
			++i;
		i--;
		if ((av[1][i] == ' ') || (av[1][i] == '\t'))
		{
			while (av[1][i] == ' ')
				i--;
		}
		while (av[1][i] != ' ')
		{
			i--;
			if (i < 0)
				break ;
		}
		i++;
		while (av[1][i] != ' ' && av[1][i] != '\0')
		{
			write(1, &av[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
